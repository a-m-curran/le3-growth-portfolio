const llm = () => getClient()

/**
 * Parse JSON from an LLM response. Claude and other models frequently
 * wrap structured output in ```json ... ``` fences despite instructions
 * not to, so we strip common fence patterns before parsing. Falls back
 * to the supplied default if parsing still fails.
 *
 * Order of attempts:
 *   1. Raw parse (happy path when the model actually obeys the prompt)
 *   2. Strip surrounding markdown code fences, parse again
 *   3. Extract the first balanced {} or [] block in the string, parse
 *   4. Return the fallback
 */
function parseJsonFromLLM<T>(text: string, fallback: T): T {
  if (!text) return fallback

  // 1. Happy path
  try {
    return JSON.parse(text) as T
  } catch {
    /* fall through */
  }

  // 2. Strip markdown code fences
  const stripped = text
    .trim()
    .replace(/^```(?:json)?\s*\n?/i, '')
    .replace(/\n?\s*```\s*$/i, '')
    .trim()
  if (stripped !== text.trim()) {
    try {
      return JSON.parse(stripped) as T
    } catch {
      /* fall through */
    }
  }

  // 3. Extract the first {...} or [...] block. This is naive but
  // sufficient for the LLM outputs we actually see.
  const blockMatch = text.match(/([{\[][\s\S]*[}\]])/)
  if (blockMatch) {
    try {
      return JSON.parse(blockMatch[1]) as T
    } catch {
      /* fall through */
    }
  }

  return fallback
}

